package it.webred.siso.ws.client.anag.client;

public class RicercaAnagraficaBean {
	private String codiceFiscale;
	private String username;
	private String password;
	private String cognomePaziente;
	private String nomePaziente;
	private String idPaziente;
	private String sesso;
	private String dataNascitaDa;
	private String dataNascitaA;
	
	public String getCodiceFiscale() {
		return codiceFiscale;
	}
	public void setCodiceFiscale(String codiceFiscale) {
		this.codiceFiscale = codiceFiscale;
	}
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getCognomePaziente() {
		return cognomePaziente;
	}
	public void setCognomePaziente(String cognomePaziente) {
		this.cognomePaziente = cognomePaziente;
	}
	public String getNomePaziente() {
		return nomePaziente;
	}
	public void setNomePaziente(String nomePaziente) {
		this.nomePaziente = nomePaziente;
	}
	public String getIdPaziente() {
		return idPaziente;
	}
	public void setIdPaziente(String idPaziente) {
		this.idPaziente = idPaziente;
	}
	public String getSesso() {
		return sesso;
	}
	public void setSesso(String sesso) {
		this.sesso = sesso;
	}
	public String getDataNascitaDa() {
		return dataNascitaDa;
	}
	public void setDataNascitaDa(String dataNascitaDa) {
		this.dataNascitaDa = dataNascitaDa;
	}
	public String getDataNascitaA() {
		return dataNascitaA;
	}
	public void setDataNascitaA(String dataNascitaA) {
		this.dataNascitaA = dataNascitaA;
	}
	
}
