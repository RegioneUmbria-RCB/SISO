package it.webred.ss.web.bean.report.dto;

public class LinguaAutovalutazione {
	
	private String comprensione = "-";
	private String lettura = "-";
	private String parlato = "-";
	private String scrittura = "-";



	public String getComprensione() {
		return comprensione;
	}



	public void setComprensione(String comprensione) {
		this.comprensione = comprensione;
	}



	public String getLettura() {
		return lettura;
	}



	public void setLettura(String lettura) {
		this.lettura = lettura;
	}



	public String getParlato() {
		return parlato;
	}



	public void setParlato(String parlato) {
		this.parlato = parlato;
	}



	public String getScrittura() {
		return scrittura;
	}



	public void setScrittura(String scrittura) {
		this.scrittura = scrittura;
	}

}
