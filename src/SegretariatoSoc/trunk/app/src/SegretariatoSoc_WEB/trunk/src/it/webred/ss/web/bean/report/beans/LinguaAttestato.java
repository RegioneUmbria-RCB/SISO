package it.webred.ss.web.bean.report.beans;

public class LinguaAttestato {

	private String rilasciatoDa = "-";
	private String comune = "-";
	private String livello = "-";



	public String getRilasciatoDa() {
		return rilasciatoDa;
	}



	public void setRilasciatoDa(String rilasciatoDa) {
		this.rilasciatoDa = rilasciatoDa;
	}



	public String getComune() {
		return comune;
	}



	public void setComune(String comune) {
		this.comune = comune;
	}



	public String getLivello() {
		return livello;
	}



	public void setLivello(String livello) {
		this.livello = livello;
	}

}
