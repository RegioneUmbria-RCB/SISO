package it.webred.ss.web.bean.report.beans;

public class ArrivoInItalia {

	private String presenza = "-";
	private String annoPrimoArrivo = "-";
	private boolean daSolo;
	private String annoPrimoPermessoDiSoggiorno = "-";
	private String valicoDiFrontiera = "-";
	private String ultimoPaeseDiProvenienza = "-";



	public String getPresenza() {
		return presenza;
	}



	public void setPresenza(String presenza) {
		this.presenza = presenza;
	}



	public String getAnnoPrimoArrivo() {
		return annoPrimoArrivo;
	}



	public void setAnnoPrimoArrivo(String annoPrimoArrivo) {
		this.annoPrimoArrivo = annoPrimoArrivo;
	}



	public boolean isDaSolo() {
		return daSolo;
	}



	public void setDaSolo(boolean daSolo) {
		this.daSolo = daSolo;
	}



	public String getAnnoPrimoPermessoDiSoggiorno() {
		return annoPrimoPermessoDiSoggiorno;
	}



	public void setAnnoPrimoPermessoDiSoggiorno(String annoPrimoPermessoDiSoggiorno) {
		this.annoPrimoPermessoDiSoggiorno = annoPrimoPermessoDiSoggiorno;
	}



	public String getValicoDiFrontiera() {
		return valicoDiFrontiera;
	}



	public void setValicoDiFrontiera(String valicoDiFrontiera) {
		this.valicoDiFrontiera = valicoDiFrontiera;
	}



	public String getUltimoPaeseDiProvenienza() {
		return ultimoPaeseDiProvenienza;
	}



	public void setUltimoPaeseDiProvenienza(String ultimoPaeseDiProvenienza) {
		this.ultimoPaeseDiProvenienza = ultimoPaeseDiProvenienza;
	}

}
